/** Опции пагинации */
export interface PaginationOptions {
  limit?: number;
  offset?: number;
  page?: number;
}

/** Результат с пагинацией */
export interface PaginatedToursResult {
  tours: Tour[];
  total: number;
  hasMore: boolean;
  page?: number;
  limit?: number;
}

const DEFAULT_PAGE_SIZE = 20;

/** Получить все туры (с пагинацией) */
export function getAllTours(options?: PaginationOptions): Tour[] | PaginatedToursResult {
  if (!options || (options.limit == null && options.offset == null && options.page == null)) {
    return TOURS_DATA;
  }
  const limit = options.limit ?? DEFAULT_PAGE_SIZE;
  const page = options.page ?? 1;
  const offset = options.offset ?? (page - 1) * limit;
  const slice = TOURS_DATA.slice(offset, offset + limit);
  return {
    tours: slice,
    total: TOURS_DATA.length,
    hasMore: offset + slice.length < TOURS_DATA.length,
    page,
    limit,
  };
}

// Получить туры по стране
export const getToursByCountry = (country: string): Tour[] => {
  return TOURS_DATA.filter(tour => tour.country === country);
};

// Получить туры по категории
export const getToursByCategory = (category: string): Tour[] => {
  return TOURS_DATA.filter(tour => tour.category === category);
};

// Получить популярные туры
export const getPopularTours = (): Tour[] => {
  return TOURS_DATA.filter(tour => tour.rating >= 4.7 && tour.reviews > 200);
};

// Поиск туров
export const searchTours = (query: string): Tour[] => {
  const lowerQuery = query.toLowerCase();
  return TOURS_DATA.filter(tour =>
    tour.title.toLowerCase().includes(lowerQuery) ||
    tour.description.toLowerCase().includes(lowerQuery) ||
    tour.country.toLowerCase().includes(lowerQuery) ||
    tour.location.toLowerCase().includes(lowerQuery) ||
    tour.tags.some((tag: string) => tag.toLowerCase().includes(lowerQuery))
  );
};

// Получить тур по ID
export const getTourById = (id: string): Tour | undefined => {
  return TOURS_DATA.find(tour => tour.id === id);
};

// Получить горящие туры
export const getHotDeals = (): Tour[] => {
  return TOURS_DATA.filter(tour => tour.hotDeal);
};

// Получить последние минуты
export const getLastMinute = (): Tour[] => {
  return TOURS_DATA.filter(tour => tour.lastMinute);
};