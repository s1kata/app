// Получить популярные страны
export const getPopularCountries = (): CountryData[] => {
  return COUNTRIES_LIST.filter(c => c.popular);
};

// Получить все страны (кроме популярных)
export const getOtherCountries = (): CountryData[] => {
  return COUNTRIES_LIST.filter(c => !c.popular).sort((a, b) => a.name.localeCompare(b.name, 'ru'));
};

// Получить страну по slug
export const getCountryBySlug = (slug: string): CountryData | undefined => {
  return COUNTRIES_LIST.find(c => c.slug === slug);
};

// Получить все страны
export const getAllCountries = (): CountryData[] => {
  return COUNTRIES_LIST;
};


